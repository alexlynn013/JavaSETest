package with_ide;

public class Q2SwapTwoNumber {

    public static void main(String[] args) {
        int a = 5;
        int b= 6;

        System.out.println(a);
        System.out.println(b);

        int swap = a;
        a = b;
        b = swap;

        System.out.println(a);
        System.out.println(b);
    }
}
