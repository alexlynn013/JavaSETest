package with_ide;

public class Q6SwichCaseStr {

    public static void main(String[] args) {
        String str = "two";

        switch(str){

            case "one" :
                System.out.println("This is one");
                break;
            case "two" :
                System.out.println("This is two");
                break;
            default:
                System.out.println("no matter");

        };
    }
}
